//checking size of primite datatypes: int, char, float, boolean
#include<iostream>
using namespace std;
int main()
{
	int a;
	char b;
	float c;
	bool d;
	short int e;
	long int f;
	cout<<"size of integer is "<<sizeof(a)<<endl;
	cout<<"size of char is "<<sizeof(b)<<endl;
	cout<<"size of float is "<<sizeof(c)<<endl;
	cout<<"size of bool is "<<sizeof(d)<<endl;
	cout<<"size of short int is "<<sizeof(e)<<endl;
	cout<<"size of long int is "<<sizeof(f)<<endl;
}
